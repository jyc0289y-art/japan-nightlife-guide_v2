def add_furigana(text):
  """HTML 문자열 내 일본어 한자에 괄호 후리가나 추가"""
  for kanji, reading in _FURIGANA_MAP:
    tagged = f"{kanji}(<span class='furigana'>{reading}</span>)"
    if kanji in text and tagged not in text:
      text = text.replace(kanji, tagged, 1)  # 첫 번째 등장만 치환
  return text

def apply_furigana_to_overlay(data_dict):
  """오버레이 데이터의 detail/strategy 필드에 후리가나 적용"""
  import copy
  result = copy.deepcopy(data_dict)
  for ck in result:
    for cat in result[ck]:
      for field in ["detail","strategy"]:
        if field in result[ck][cat]:
          result[ck][cat][field] = add_furigana(result[ck][cat][field])
  return result


